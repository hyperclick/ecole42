int	ft_count_if(char **tab, int (*f)(char*))
{
	int r;

	if (tab <= 0 || f <= 0)
	{
		return (0);
	}
	r = 0;
	while (*tab != 0)
	{
		if (f(*tab))
		{
			++r;
		}
		++tab;
	}
	return (r);
}
