#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;
	unsigned char	ch;

	str = (unsigned char*)s;
	if (n == 0)
	{
		return (NULL);
	}
	ch = (unsigned char)c;
	if (*str == ch)
	{
		return (str);
	}
	return (ft_memchr(str + 1, c, n - 1));
}
