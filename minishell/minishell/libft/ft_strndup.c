#include "libft.h"

char	*ft_strndup(const char *s1, int len)
{
	char	*r;

	len = MIN(len, ft_strlen(s1));
	r = malloc(sizeof(char) * (len + 1));
	if (r == NULL)
	{
		return (NULL);
	}
	r[len] = 0;
	return (ft_memcpy(r, s1, len));
}
